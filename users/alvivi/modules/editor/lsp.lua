  {'╭', "FloatBorder"},
  {'─', "FloatBorder"},
  {'╮', "FloatBorder"},
  {'│', "FloatBorder"},
  {'╯', "FloatBorder"},
  {'─', "FloatBorder"},
  {'╰', "FloatBorder"},
  {'│', "FloatBorder"},
